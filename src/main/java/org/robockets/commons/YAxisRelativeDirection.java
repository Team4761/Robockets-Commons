package org.robockets.commons;

/**
 * Represents a relative direction on the y-axis.
 * @author Simon Andrews
 * @since 0.0.1
 * @see XAxisRelativeDirection
 * @see ZAxisRelativeDirection
 */
public enum YAxisRelativeDirection {
	/**
	 * Relative horizontal direction opposed to backward.
	 */
	FORWARD,
	
	/**
	 * Relative horizontal direction opposed for forward.
	 */
	BACKWARD,
}
