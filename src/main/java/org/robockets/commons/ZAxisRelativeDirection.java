package org.robockets.commons;

/**
 * Represents a relative direction on the z-axis.
 * @author Simon Andrews
 * @since 0.0.1
 * @see XAxisRelativeDirection
 * @see YAxisRelativeDirection
 */
public enum ZAxisRelativeDirection {
	/**
	 * Relative vertical direction opposed to down.
	 */
	UP,
	
	/**
	 * Relative vertical direction opposed to up.
	 */
	DOWN,
}
