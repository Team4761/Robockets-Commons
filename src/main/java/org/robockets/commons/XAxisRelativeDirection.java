package org.robockets.commons;

/**
 * Represents a relative direction on the x-axis.
 * @author Simon Andrews
 * @since 0.0.1
 * @see YAxisRelativeDirection
 * @see ZAxisRelativeDirection
 */
public enum XAxisRelativeDirection {
	/**
	 * Relative horizontal direction opposed to right.
	 */
	LEFT,
	
	/**
	 * Relative horizontal direction opposed to left.
	 */
	RIGHT,
}
